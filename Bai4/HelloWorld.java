package Bai4;

public class HelloWorld {
    public static void main(String[] args) {
        // Sử dụng System.out.print() (không xuống dòng)
        System.out.print("Hello World! (print) ");

        // Sử dụng System.out.println() (có xuống dòng)
        System.out.println("Hello World! (println)");

        // Sử dụng System.out.printf() (định dạng, có xuống dòng)
        System.out.printf("Hello World! (printf)%n");
    }
}
